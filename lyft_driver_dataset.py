def km_return_probability(
    durations,
    return_events,
    time_point
):
    km_data = pd.DataFrame({
        "duration": durations,
        "return_event": return_events
    }).sort_values("duration")

    survival_probability = 1.0

    event_times = sorted(
        km_data.loc[
            km_data["return_event"] == 1,
            "duration"
        ].unique()
    )

    for event_time in event_times:

        if event_time > time_point:
            break

        drivers_at_risk = (
            km_data["duration"] >= event_time
        ).sum()

        returns_at_time = (
            (km_data["duration"] == event_time)
            & (km_data["return_event"] == 1)
        ).sum()

        survival_probability *= (
            1 - returns_at_time / drivers_at_risk
        )

    return 1 - survival_probability

