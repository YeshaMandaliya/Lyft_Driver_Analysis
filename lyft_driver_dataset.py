post_event_tables = {}

for threshold, outcome_table in outcome_tables.items():

    event_records = outcome_table[
        outcome_table["event_observed"] == 1
    ].copy()

    post_event_durations = []
    return_events = []

    for _, row in event_records.iterrows():

        ride_times = driver_ride_times[row["driver_id"]]

        future_rides = [
            ride_time
            for ride_time in ride_times
            if ride_time > row["event_at"]
        ]

        if len(future_rides) > 0:
            next_return = min(future_rides)
            duration = (
                next_return - row["event_at"]
            ).total_seconds() / 86400
            return_event = 1
        else:
            duration = (
                observation_end - row["event_at"]
            ).total_seconds() / 86400
            return_event = 0

        post_event_durations.append(duration)
        return_events.append(return_event)

    event_records["post_event_followup_days"] = (
        post_event_durations
    )

    event_records["return_observed"] = return_events

    post_event_tables[threshold] = event_records

