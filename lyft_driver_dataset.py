# Step 4.9 — Cohort-preserving validation of the Cox challenger

from lifelines import CoxPHFitter
from lifelines.utils import concordance_index

def validate_cox_by_cohort(
    model_data,
    predictors,
    landmark_day,
    penalizer=0.10
):

    fold_results = []

    for heldout_cohort in sorted(
        model_data["onboard_week"].unique()
    ):

        train = model_data[
            model_data["onboard_week"] != heldout_cohort
        ].copy()

        test = model_data[
            model_data["onboard_week"] == heldout_cohort
        ].copy()

        # Learn preprocessing only from the training cohorts
        training_medians = train[predictors].median()

        train_x = train[predictors].fillna(training_medians)
        test_x = test[predictors].fillna(training_medians)

        training_means = train_x.mean()
        training_stds = train_x.std(ddof=0).replace(0, 1)

        train_x = (
            train_x - training_means
        ) / training_stds

        test_x = (
            test_x - training_means
        ) / training_stds

        cox_training_data = train_x.copy()
        cox_training_data["followup_days"] = train[
            "followup_days"
        ].values
        cox_training_data["event_observed"] = train[
            "event_observed"
        ].values

        cph = CoxPHFitter(penalizer=penalizer)

        cph.fit(
            cox_training_data,
            duration_col="followup_days",
            event_col="event_observed",
            show_progress=False
        )

        predicted_hazard = (
            cph.predict_partial_hazard(test_x)
            .to_numpy()
            .ravel()
        )

        model_c_index = concordance_index(
            test["followup_days"],
            -predicted_hazard,
            test["event_observed"]
        )

        current_gap_c_index = concordance_index(
            test["followup_days"],
            -test["current_gap_days"],
            test["event_observed"]
        )

        active_day_c_index = concordance_index(
            test["followup_days"],
            test["active_day_rate"],
            test["event_observed"]
        )

        fold_results.append({
            "landmark_day": landmark_day,
            "heldout_cohort": heldout_cohort,
            "test_drivers": len(test),
            "test_events": int(test["event_observed"].sum()),
            "cox_c_index": model_c_index,
            "current_gap_c_index": current_gap_c_index,
            "active_day_c_index": active_day_c_index
        })

    return pd.DataFrame(fold_results)


