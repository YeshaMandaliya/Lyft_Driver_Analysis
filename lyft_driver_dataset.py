def create_inactivity_outcome(
    data,
    landmark_day,
    inactivity_days,
    onboard_timezone="UTC"
):
    records = []

    observation_end = data["requested_at"].max()
    inactivity_period = pd.Timedelta(days=inactivity_days)

    for driver_id, driver_data in data.groupby("driver_id"):

        driver_data = driver_data.sort_values("requested_at")

        # The onboarding field is date-only.
        # UTC is the primary alignment convention.
        onboard_date = (
            pd.Timestamp(
                driver_data["onboard_date"].iloc[0]
            )
            .tz_localize(onboard_timezone)
            .tz_convert("UTC")
        )

        landmark_at = onboard_date + pd.Timedelta(
            days=landmark_day
        )

        ride_times = (
            driver_data["requested_at"]
            .sort_values()
            .tolist()
        )

        # A driver must have at least one ride
        # before the prediction landmark.
        pre_landmark_rides = [
            ride_time
            for ride_time in ride_times
            if ride_time < landmark_at
        ]

        if len(pre_landmark_rides) == 0:
            continue

        current_gap_days = (
            landmark_at
            - max(pre_landmark_rides)
        ).total_seconds() / 86400

        # Exclude only if the CURRENT gap has already
        # crossed the threshold at the landmark.
        if current_gap_days >= inactivity_days:
            continue

        first_event_at = None
        returned_after_event = False

        for position, ride_time in enumerate(ride_times):

            if position < len(ride_times) - 1:
                next_ride = ride_times[position + 1]
            else:
                next_ride = None

            threshold_crossing = (
                ride_time + inactivity_period
            )

            # Historical resolved episodes do not make
            # the driver permanently ineligible.
            if threshold_crossing <= landmark_at:
                continue

            # The event must be observable before
            # administrative censoring.
            if threshold_crossing > observation_end:
                continue

            # The gap reaches the threshold only if no
            # subsequent ride occurs before crossing.
            gap_reaches_threshold = (
                next_ride is None
                or next_ride >= threshold_crossing
            )

            if gap_reaches_threshold:
                first_event_at = threshold_crossing
                returned_after_event = next_ride is not None
                break

        event_observed = first_event_at is not None

        if event_observed:
            followup_days = (
                first_event_at - landmark_at
            ).total_seconds() / 86400
        else:
            followup_days = (
                observation_end - landmark_at
            ).total_seconds() / 86400

        records.append({
            "driver_id": driver_id,
            "landmark_day": landmark_day,
            "inactivity_days": inactivity_days,
            "current_gap_days": current_gap_days,
            "event_observed": int(event_observed),
            "followup_days": followup_days,
            "event_at": first_event_at,
            "returned_after_event": (
                int(returned_after_event)
                if event_observed
                else np.nan
            )
        })

    return pd.DataFrame(records)

